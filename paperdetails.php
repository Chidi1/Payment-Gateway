//==============================================================================
// Verifies a discount code
// @return  JSON    discount
//==============================================================================
function validateDiscountCode(strDiscount){
    
    // Verify discount code based on known structure
    // Check discound code length
    if (strDiscount.length != 8){
        discount = {
            status:false,
            percentage: false,
            response:'Invalid discount code.'
        }
        
        return discount;
    }

    // Check structure of discount code
    var pattern = /[a-z]{4}[0-9]{4}/;
    var status = pattern.test(strDiscount);
    if (status === false){
        discount = {
            status:false,
            percentage: false,
            response:'Invalid discount code.'
        }
        
        return discount;
    }
    // Discount code has passed structural tests. Read cookie valid_codes
    var strValidCodes = $.cookie('valid_codes');
    var serializedValidCodes = atob(strValidCodes);
    var validCodes = phpUnserialize(serializedValidCodes);
    // Lookup discount. First check if discount code is defined in discount for
    // all, if not in that array check in lifetime_discounts array. If code not
    // found; contact server for verification of discount code.
    var found = false;
    var index = false;
    for (var i=0; i<validCodes.length; i++){
        if (validCodes[i][strDiscount]){
            found = true;
            index = i;
            break;
        }
    }
    
    if (found === true){
        // Get discount percentage
        discountValue = Number(validCodes[i][strDiscount]);
        // Set a discount object response to return
        discount = {
            status:true,
            percentage: discountValue,   // Discount in percentage effected
            response:false
        }
        // Return discount response
        return discount;
    }
    else if (found === false){
        // Discount code not among valid codes. Check with server
        var discount ={status:false, percentage:false, response:false};
        var resp = serverValidateDiscount(strDiscount);
        if (resp.status === true){
            discount.status = resp.status;
            discount.percentage = ((100 - resp.percentage)/100);
            // Add discount information to discounts array of valid codes
            var newIndex = new Array();
            newIndex[strDiscount] = discount.percentage;
            validCodes.push(newIndex);
            var serializedValidCodes = serialize(validCodes);
            var base64_encoded = btoa(serializedValidCodes);
            // Remove original cookie
            if ($.removeCookie('valid_codes')){
                // Update cookie file
                $.cookie('valid_codes', base64_encoded, {expires: 1, path: '/'});
            }
        }
        else if (resp.status === false)
            discount.response = resp.response;
        
        return discount;
    }
}
//==============================================================================
// Server validates discount code passed
// @param   String  discount_code      Code to be validated on server side
// @return  Object  discount           Discount code information
//==============================================================================
function serverValidateDiscount(discount_code){
    var actionUrl = $('#validation_url').val();
    $.ajax({
        async:false,
        type:'POST',
        url:actionUrl,
        data:{code: discount_code},
        dataType:'json',
        cache:false,
