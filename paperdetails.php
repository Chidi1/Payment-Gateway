// Block the user interface with a message if the form's data is valid.
//==============================================================================
function submitIfValid(form, messageToShow) {
    valid = form.validationEngine('validate');
    if (valid) {
        blockUserInterface(messageToShow);
        // Remove applied discount code from cookie file. Start checking at 
        // second index, first index will always be occupied by discount_for_all
        // which we want to keep.
        var discountCode = $('#discount-code').val();
        if ((discountCode != '') && (discountCode != 'Optional field') 
            && (customer_loyalty_program != 0)){
            var strValidCodes = $.cookie('valid_codes');
            var serializedValidCodes = atob(strValidCodes);
            var validCodes = phpUnserialize(serializedValidCodes);
            for (var i=1; i<validCodes.length; i++){
                if (validCodes[i][discountCode]){
                    delete validCodes[i];
                    break;
                }
            }
            // Update cookie file by removing existing cookie file and setting
            // a new one.
            if ($.removeCookie('valid_codes')){
                var serializedValidCodes = serialize(validCodes);
                var base64_encoded = btoa(serializedValidCodes);
                $.cookie('valid_codes', base64_encoded, {expires: 1, path: '/'});
            }
        }
        form.submit();
    }
    return false;
}

