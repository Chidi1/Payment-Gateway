// Used to calculate the price of an order.
//==============================================================================
function calculatePrice() {
    typeOfWriting = $('#writing-type').val();
    typeOfPaper = $('#service-type').val();
    academicLevel = $('#academic-level').val();
    deadline = $('#deadline').val();
    numberOfPages = $('#pages').val();
    numberOfSlides = $('#slides').val();
    var discountCode = $('#discount-code').val();
    // Only calculate the price if the user has typed a valid (integer) for the 
    // number of pages.
    if (academicLevel && deadline && isInteger(numberOfPages)) {
        // Determine the windows.prices array index that will give us the
        // price corresponding to the user's selections.
        
        // First check the service type (type of paper). If it's Admission Essay
        // or Application Letter, set the academic level to 'Admission'.
        if ((typeOfPaper == 'Admission Essay') 
                                     || (typeOfPaper == 'Application Letter'))
            academicLevel = 'Admission';
        var index = typeOfWriting + '_' + academicLevel + '_' 
                    + window.deadlineMap[deadline];
        pricePerPage = window.prices[index];
        pagesPrice = pricePerPage * numberOfPages;
        if (isInteger(numberOfSlides))
            slidesPrice = 0.5 * pricePerPage * numberOfSlides;
        else 
            slidesPrice = 0;
        totalPrice = pagesPrice + slidesPrice;
        // Check if customer loyalty scheme is running, if not apply a 15%
        // discount when calculating price
        if (customer_loyalty_program == 0){
            // Currently, a discount of 15% is being effected.
            discountedPrice = roundNumber(0.85 * totalPrice, 2);
            $('#price').html('â‚¦' + totalPrice);
            $('#discountedprice').html('â‚¦' + discountedPrice);

            document.getElementById("discountedpricenew").value = discountedPrice;
            $('#price').addClass('cancel-cross');

        }
        // Check if a discount code is available and that totalPrice is not less
        // than 1.
        else if (discountCode != '' && discountCode != 'Optional field' && totalPrice > 0){
            // Check if discount is valid and discount order price
            var discount = validateDiscountCode(discountCode);
            if (discount.status === true){
                // Remove discount status error msgs
                $('#discount-error').empty();
                // Show discount percentage applied
                var d = Math.round((1 - discount.percentage) * 100);
                if ($('#discount-error').hasClass('red-text'))
                    $('#discount-error').removeClass('red-text');
                if (d != 0){
                    $('#discount-error').html(d + "% discount applied.")
                        .addClass('green-text');
                    discountedPrice = roundNumber(discount.percentage * totalPrice, 2);
                    $('#price').html('â‚¦' + totalPrice);
                    $('#discountedprice').html('â‚¦' + discountedPrice);
                    document.getElementById("discountedpricenew").value = discountedPrice;
                    $('#price').addClass('cancel-cross');
                }
                else {
                    // No discount effected.
                    $('#price').html('â‚¦' + totalPrice);
                }
            }
            else if (discount.status === false){
                // Remove discounted price that was set
                $('#discountedprice').empty();
                // Show cost for order
                $('#price').html('â‚¦' + totalPrice);
                $('#price').removeClass('cancel-cross');
                // Notify user why a discount is not available
                if ($('#discount-error').hasClass('green-text'))
                    $('#discount-error').removeClass('green-text');
                $('#discount-error').html(discount.response).addClass('red-text');
            }
        }
