// Code that allows a user to read comments. Uses jQuery Ajax to connect to the 
// system and flag comment as read.
//==============================================================================
function readComment() {
    var lessContentChars = 3;
    var ellipsesText = ".....";
    var moreLinkText = "Read";
    // Loop through all comments. Divide each comment it into two parts. 
    // This section gives each comment the "<first_part>..... Read" appearance.
    $('.read').each(function() {
        var content = $(this).html();
        // Remove any leading and trailing white spaces.
        content = jQuery.trim(content);
        if(content.length > lessContentChars) {
            // Obtain the few viewable characters in string.
            var lessContent = content.substr(0, lessContentChars);
            // Obtain the rest of the characters.
            var moreContent = content.substr(lessContentChars); 
            var html = lessContent + '<span class="more_ellipses">' 
                        + ellipsesText + '</span><span class="more_content">'
                        + '<span>' + moreContent + '</span>&nbsp;'
                        + '<a href="" class="more_link">' + moreLinkText 
                        + '</a></span>'; 
            $(this).html(html);
        } 
    });

    $(".more_link").click(function(){
        // Block the UI during the Ajax call.
        blockUserInterface('Retrieving.....');
        // Get the value of href attribute of respective .read_comment_link.
        // It's important not to select the element by class since there may be 
        // numerous comments each with it's own .read_comment_link.
        var readCommentUrl = $(this).parent().parent().prev().attr('href');
        var moreLink = $(this);
        $.ajax({
            url: readCommentUrl,
            cache: false,
            success: function(response) {
                $('#jquery-response').html(response);
                var readCommentResult = $('#jquery-result').html();
                if (readCommentResult == 'Success') {
                    // hide moreLinkText
                    moreLink.toggle();
                    // Hide ellipsesText
                    moreLink.parent().prev().toggle();
                    // Show moreContent
                    moreLink.prev().toggle();
                    // Change td style.
                    moreLink.parent().parent().parent().attr('class', 
                                                            'already_read_td'); 
                } else {
                    // Order may have been reassigned, comment deleted or 
                    // user privileges to view the comment may have changed. 
                    unexpectedError = "Sorry! An unexpected error occured while"
                                      + " retrieving the comment. Please "
                                      + "refresh the page and try again.";
                    $('#jquery-error').html(unexpectedError);
                    $('#jquery-error-dialog-box').dialog('open');
                }
            },
            error: function(xhr) {
                // We are having connectivity issues.
                connectivityError = "Sorry! There seems to be a connectivity  "
                            + "issue. Please ensure that you are connected "
                            + "to the internet and try again.";
                $('#jquery-error').html(connectivityError);
                $('#jquery-error-dialog-box').dialog('open');
            }
        });      
        // Now unblock the UI.
        $.unblockUI(); 
        return false;          
    });
}

